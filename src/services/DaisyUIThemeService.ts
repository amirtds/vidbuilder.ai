/**
 * Get theme colors for a given DaisyUI theme
 */
export function getThemeColors(themeName: DaisyUITheme): ThemeColors {
  return THEME_COLORS[themeName] || THEME_COLORS.light;
}

/**
 * Convert theme to style object for scenes
 * Following DESIGN.md: No gradients, use system fonts
 */
export function themeToSceneStyle(themeName: DaisyUITheme): ThemeColors {
  const colors = getThemeColors(themeName);
  
  // Per DESIGN.md: Use system fonts, never hard-code font names
  return {
    ...colors,
    fontFamily: '-apple-system, BlinkMacSystemFont, "Segoe UI", Roboto, Helvetica, Arial, sans-serif',
  };
}

/**
 * Check if a theme is dark mode
 */
export function isDarkTheme(themeName: DaisyUITheme): boolean {
  const darkThemes: DaisyUITheme[] = [
    'dark',
    'synthwave',
    'halloween',
    'forest',
    'black',
    'luxury',
    'dracula',
    'business',
    'night',
    'coffee',
    'dim',
    'nord',
    'sunset',
    'vidbuilder'
  ];
  return darkThemes.includes(themeName);
}
